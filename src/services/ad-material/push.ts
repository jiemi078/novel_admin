const includes = (source: string | number | undefined, keyword: string) =>
  String(source || '').toLowerCase().includes(keyword);

export async function queryAdMaterialPushPage(params: AdMaterialPushPageParams) {
  const current = Number(params.current || params.page || 1);
  const pageSize = Number(params.pageSize || 20);
  const keyword = String(params.keyword || '').trim().toLowerCase();
  const status = params.status === undefined ? -1 : Number(params.status);

  const filtered = rows.filter((item) => {
    if (params.contentType && item.contentType !== Number(params.contentType)) return false;
    if (params.platformCode && item.platformCode !== params.platformCode) return false;
    if (status !== -1 && item.status !== status) return false;
    if (!keyword) return true;
    return (
      includes(item.id, keyword) ||
      includes(item.contentProjectName, keyword) ||
      includes(item.materialCreatorName, keyword) ||
      includes(item.createdByName, keyword)
    );
  });

  return {
    data: filtered.slice((current - 1) * pageSize, current * pageSize),
    total: filtered.length,
    success: true,
  };
}

